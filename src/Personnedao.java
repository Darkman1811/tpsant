public class Personnedao{
	private int id;
	private String nom;
	private int age;

	public void setId(int id){
		this.id=id;
	}

	public int getId(){
		return this.id;
	}

	public void setNom(String nom){
		this.nom=nom;
	}

	public String getNom(){
		return this.nom;
	}

	public void setAge(int age){
		this.age=age;
	}

	public int getAge(){
		return this.age;
	}
}